#include <iostream>
#include <math.h>
using namespace std;

int main(){
  int toes = 10;
  double toes_2 = toes * 2;
  double toes_square = pow(toes, 2);
  cout << "toes：" << toes << endl;
  cout << "toes的两倍：" << toes_2 << endl;
  cout << "toes的平方：" << toes_square << endl;
  return 0;
}